package frc.robot.subsystems;

/** Add your docs here. */
public class ArmWaypoint {

    private double x;
    private double z;

    public ArmWaypoint(double x, double z) {
        this.x = x;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getZ() {
        return z;
    }

}
